    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // VIEW BUTTON
         String pid = pId.getText();
       // int patientId = Integer.parseInt(pid);
        try{
            
            DefaultTableModel model = (DefaultTableModel)Table.getModel();
            
            // load driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            // connection
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Hospital","root","password"); 
         //  String query = "SELECT * FROM WHERE patientId==?";
           PreparedStatement ps = con.prepareStatement("SELECT * FROM  patient WHERE id=?");
           ps.setString(1,pid);
           ResultSet rs = ps.executeQuery();
           while(rs.next()){
               int patientid = rs.getInt("id");
               String pID = Integer.toString(patientid);
               String pName = rs.getString("Name");
               int pAge = rs.getInt("age");
               String page = Integer.toString(pAge);
               String dName = rs.getString("doctor");
               
               String row[] = {pID,pName,page,dName};
               model.addRow(row);
           }
           con.close();
           
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

